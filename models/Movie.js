class Movie {
  constructor(id, name, description, imageUrl, backdropImage, generes, rating) {
    this.id = id;
    this.name = name;
    this.description = description;
    this.imageUrl = imageUrl;
    this.backdropImage = backdropImage;
    this.generes = generes;
    this.rating = rating;
  }
}

export default Movie;
